s = "Hello"
print(s[0])
print(len(s))

# The line `print(s[len(s) - 1])` is printing the last character of the string `s`.
print(s[len(s) - 1])

# `print(s[-1])` is printing the last character of the string `s`. In Python, negative indexing allows
# you to access elements from the end of the string. So, `s[-1]` refers to the last character of the
# string `s`.
print(s[-1])